int	ft_atoi(const char *str)

{
	int	i;
	int	j;
	int	sig;
	
	i = 0;
	j = 0;
	sig = 1;
	
		while (str[i] <= 32 && str[i] != 127 && str[i] != '\0')
		{
			i++;
		}
		while (str[i] >= 43 && str[i] <= 47 && str[i] != '\0'  && str[i] != 44 && str[i] != 46 && str[i] != 47)
		{
			if (str[i] == 45)
			{
				sig *= -1;
			}
			i++;
		}
		while (str[i] >= 48 && str[i] <= 57  && str[i] != '\0')
		{
			j *= 10;
			j += str[i] -48;
			i++;
		}
		j = j* sig;
		return (j);
}
